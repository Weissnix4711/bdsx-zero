/* -------------------------------------------------------------------------- */
/*                                 JS Command                                 */
/* -------------------------------------------------------------------------- */
/* Eval any javascript. Command is />                                         */
/* -------------------------------------------------------------------------- */

/* --------------------------------- Imports -------------------------------- */

import { command } from 'bdsx';

/* ---------------------------------- Main ---------------------------------- */

command.hook.on((command, originName) => {
  if (!command.startsWith('/>')) {return;}
  try {
    eval(command.substr(2));
  } catch (err) {
    throw(err);
  }
});

